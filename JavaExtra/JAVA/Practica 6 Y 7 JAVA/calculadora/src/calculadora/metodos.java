package calculadora;

public class metodos {
    
    public double sumar (double n1, double n2){
        double resultado = 0;
        resultado = n1 + n2;
        return resultado;
    }
    
    public double restar (double n1, double n2){
        double resultado = 0;
        resultado = n1 - n2;
        return resultado;
    }
    
    public double multiplicar (double n1, double n2){
        double resultado = 0;
        resultado = n1 * n2;
        return resultado;
    }
    
    public double dividir (double n1, double n2){
        double resultado = 0;
        resultado = n1 / n2;
        return resultado;
    }
}
